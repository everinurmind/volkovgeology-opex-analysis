-- 1. Сводка план/факт по месторождениям и годам
SELECT 
    o.наименование_месторождения,
    d.регион,
    d.тип_бурения,
    o.год,
    COUNT(DISTINCT o.номер_скважины) AS кол_во_скважин,
    ROUND(SUM(o.план_тыс_тнг) / 1000, 1) AS план_млн_тнг,
    ROUND(SUM(o.факт_тыс_тнг) / 1000, 1) AS факт_млн_тнг,
    ROUND((SUM(o.факт_тыс_тнг) - SUM(o.план_тыс_тнг)) / 1000, 1) AS отклонение_млн,
    ROUND((SUM(o.факт_тыс_тнг) - SUM(o.план_тыс_тнг)) / NULLIF(SUM(o.план_тыс_тнг), 0) * 100, 1) AS отклонение_пцт
FROM drilling_operations o
JOIN deposits d ON o.код_месторождения = d.код_месторождения
WHERE o.код_месторождения != 'МСТ-000'
GROUP BY o.наименование_месторождения, d.регион, d.тип_бурения, o.год
ORDER BY o.наименование_месторождения, o.год;

-- 2. Стоимость метра бурения по месторождениям
SELECT 
    наименование_месторождения,
    год,
    COUNT(DISTINCT номер_скважины) AS скважин,
    ROUND(AVG(глубина_факт_м), 1) AS средняя_глубина_м,
    ROUND(SUM(факт_тыс_тнг) / SUM(глубина_факт_м), 2) AS стоимость_метра_тыс
FROM drilling_operations
WHERE код_месторождения != 'МСТ-000'
GROUP BY наименование_месторождения, год
ORDER BY наименование_месторождения, год;

-- 3. Затраты по статьям и кварталам
SELECT 
    c.наименование_статьи,
    c.группа,
    o.год,
    o.квартал,
    ROUND(SUM(o.факт_тыс_тнг) / 1000, 1) AS факт_млн_тнг,
    ROUND(SUM(o.план_тыс_тнг) / 1000, 1) AS план_млн_тнг
FROM drilling_operations o
JOIN cost_categories c ON o.код_статьи = c.код_статьи
WHERE o.код_месторождения != 'МСТ-000'
GROUP BY c.наименование_статьи, c.группа, o.год, o.квартал
ORDER BY o.год, o.квартал, c.наименование_статьи;

-- 4. Топ-20 скважин с наибольшим перерасходом
SELECT 
    номер_скважины,
    наименование_месторождения,
    глубина_факт_м,
    ROUND(SUM(план_тыс_тнг), 1) AS план_итого,
    ROUND(SUM(факт_тыс_тнг), 1) AS факт_итого,
    ROUND(SUM(факт_тыс_тнг) - SUM(план_тыс_тнг), 1) AS перерасход_тыс,
    ROUND((SUM(факт_тыс_тнг) - SUM(план_тыс_тнг)) / NULLIF(SUM(план_тыс_тнг), 0) * 100, 1) AS перерасход_пцт
FROM drilling_operations
WHERE код_месторождения != 'МСТ-000'
GROUP BY номер_скважины, наименование_месторождения, глубина_факт_м
HAVING SUM(факт_тыс_тнг) > SUM(план_тыс_тнг)
ORDER BY перерасход_пцт DESC
LIMIT 20;